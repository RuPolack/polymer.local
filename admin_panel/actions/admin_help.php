<h2>1. Управление профессиями</h2>
<ul>
    <li><strong>Добавить профессию</strong> — создайте новую профессию, которая будет доступна при создании вопросов.</li>
    <li><strong>Редактировать профессию</strong> — измените название существующей профессии.</li>
    <li><strong>Удалить профессию</strong> — удаление профессии также удаляет связанные с ней вопросы.</li>
    <li>Все действия выполняются через соответствующие формы в админ-панели.</li>
</ul>

<h2>2. Управление вопросами</h2>
<ul>
    <li><strong>Добавить вопрос</strong> — создайте новый вопрос для выбранной профессии, укажите варианты ответов и правильный вариант.</li>
    <li><strong>Редактировать вопрос</strong> — измените текст вопроса, варианты ответов или правильный ответ.</li>
    <li><strong>Удалить вопрос</strong> — удаление вопроса полностью убирает его из базы данных.</li>
</ul>

<h2>3. Просмотр записей</h2>
<ul>
    <li>Вы можете просматривать все добавленные профессии и вопросы.</li>
    <li>Используйте кнопки "Профессии" и "Вопросы" для переключения между списками.</li>
</ul>

